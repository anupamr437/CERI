package lab4exercise1;

public class Person {
	private String name;
	private float age;
	//setters
	public void setName(String newName) {
		this.name=newName;
	}
	public void setAge(int newAge) {
		this.age=newAge;
	}
	//getters
	public String getName() {
		return name;
	}
	public float getAge() {
		return age;
	}
}
